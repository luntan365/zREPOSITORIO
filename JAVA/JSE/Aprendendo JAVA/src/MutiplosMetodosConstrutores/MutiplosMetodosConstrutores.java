package MutiplosMetodosConstrutores;

public class MutiplosMetodosConstrutores {
	
	MutiplosMetodosConstrutores (){
		System.out.println("Sem parametros");		
	}

	MutiplosMetodosConstrutores (int num01){
		System.out.println("Um parâmetro: "+num01);
	}
	
	MutiplosMetodosConstrutores (int num01, int num02){
		System.out.println("Dois parâmetros: "+num01+" - "+num02);
	}
	
	MutiplosMetodosConstrutores (int num01, int num02, int num03){
		System.out.println("Três Parâmetros: "+num01+" - "+num02+" - "+num03);
	}
	
	

}
